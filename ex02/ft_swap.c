#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int c,d;
	int	*tmp;
	c = 1;
	d = 2;
	*a = c;
	*b = d;
	*tmp = *a;
	*a = *b;
	*b = *tmp;
}
